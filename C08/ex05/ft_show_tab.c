#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *str)

{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	ft_putnbr(int nb)

{
	char	*x_base;

	x_base = "0123456789";
	if (nb >= 10)
		ft_putnbr(nb / 10);
	write(1, &x_base[nb % 10], 1);
}

void	ft_show_tab(struct s_stock_str *par)

{
	int	i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		write(1, "\n", 1);
		ft_putnbr(par[i].size);
		write(1, "\n", 1);
		ft_putstr(par[i].copy);
		write(1, "\n", 1);
		i++;
	}
}
