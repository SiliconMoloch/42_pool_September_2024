int	letter(char c)

{
	if ((c >= 'A' && c <= 'Z') || ((c >= 'a' && c <= 'z')))
		return (1);
	return (0);
}

int	ft_str_is_alpha(char *str)

{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!letter(str[i]))
			return (0);
		i++;
	}
	return (1);
}
